package model;

/**
 * Enum representing the main educational stages in the Egyptian school system.
 *
 * <ul>
 *   <li>{@link #PRIMARY} – Primary stage (grades 1 to 6)</li>
 *   <li>{@link #PREPARATORY} – Preparatory stage (grades 1 to 3)</li>
 *   <li>{@link #SECONDARY} – Secondary stage (grades 1 to 3)</li>
 * </ul>
 *
 * Author: Mohamed Sayed
 * Date: 23-6-2025
 * Version: 1.0
 */
public enum Stage {
    PRIMARY,        // Primary stage (المرحلة الابتدائية)
    PREPARATORY,    // Preparatory stage (المرحلة الإعدادية)
    SECONDARY       // Secondary stage (المرحلة الثانوية)
}
